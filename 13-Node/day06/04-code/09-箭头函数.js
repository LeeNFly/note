// 箭头函数的简写形式：
// 1. 如果箭头函数的参数列表中只有一个参数, 那么参数的()可以省略
// let double = (a) => {
//     console.log(a * 2);
// }

//简写如下
// let double = a => {
//     console.log(a * 2);
// }

// double(10);

// 2. 如果箭头函数的函数体只有一句代码，那么函数体的{}可以被省略
// let func = () => {
//     console.log("Hey 春生");
// }
// 简写如下
// let func = () => console.log("Hey 春生");

// let triple = (a) => {
//     console.log(a * 3);
// }
//简写如下
// let triple = a => console.log(a * 3);

// triple(10);

// 3. 如果箭头函数的函数体只有一句代码，并且这句代码是返回语句，那么return和{}都可以省略
// let sum = (a, b) => {
//     return a + b;
// }

//简写如下
// let sum = (a, b) => a + b;

